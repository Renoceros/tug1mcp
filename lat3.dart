void main() {
  int num = 10;

  if (num > 5){
    print('Number bigger than 5');
  }
  if (num % 2 == 0){
    print('Number is even');
  }  else {
    print('Number is odd');
  }

  if (num > 10){
    print('Number is bigger than 10');
  } else if (num < 10){
    print('Number is smaller than 10');
  } else {
    print('Number is 10');
  }
}